let x = 5

if (x == 4) {
    console.log('x égal 4')
} else if (x == 5) { // si la condition précédante est fausse et que la nouvelle condition après le if est vraie
    console.log('x n\'est pas égal à 4 mais à 5')
} else { // si aucune des conditions précédantes n'est vraie
    console.log('x n\'est ni égal à 4 ni à 5')
}
